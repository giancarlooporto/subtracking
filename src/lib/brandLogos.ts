/**
 * Normalize company name for lookup
 */
export function normalizeBrandName(name: string): string {
    return name.toLowerCase().trim();
}

/**
 * Get logo URL from curated database or fallback to automatic detection
 */
export function getBrandLogo(name: string, domain?: string): string | null {
    const normalized = normalizeBrandName(name);

    // Check curated database first
    if (BRAND_LOGOS[normalized]) {
        return BRAND_LOGOS[normalized];
    }

    // Fallback to unavatar if we have a domain
    if (domain) {
        return `https://unavatar.io/${domain}`;
    }

    return null;
}
